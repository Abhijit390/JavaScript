// booleans & comparison operator 

// booleans 
// true, false 

let num1 = "7";
let num2 = 7;
console.log(num1<=num2);

// == vs === 
//if we want check only value comparions not data type then "==" used
console.log(num1 == num2); //output true

//If we want to check value alongwith data type then "===" used
console.log(num1 === num2); //output false

// != vs !==
//if we want check only value comparions not data type then "!=" used
console.log(num1 != num2); //output false

//If we want to check value alongwith data type then "!==" used
console.log(num1 !== num2); //output true