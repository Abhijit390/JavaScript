// break keyword
// if user wants to particule condition becomes true then it will break the condition terminate that block
for(let i = 1; i<=10; i++){
    if(i===4){
        break;
    }
    console.log(i);
}
console.log("hello");

// continue keyword
// particular condition becomes neglects then continue keyword is used
for(let i = 1; i<=10; i++){
    if(i===4){
        continue;
    }
    console.log(i);
}
console.log("hello there");
